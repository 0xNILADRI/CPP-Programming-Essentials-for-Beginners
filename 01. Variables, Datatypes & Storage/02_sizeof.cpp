#include<iostream>
using namespace std;

int main(){
    int a; float b; char c; bool d; double e;

    cout << "SizeOf INT          : " << sizeof(a) << endl;
    cout << "SizeOf FLOAT        : " << sizeof(b) << endl;
    cout << "SizeOf CHAR         : " << sizeof(c) << endl;
    cout << "SizeOf BOOL         : " << sizeof(d) << endl;
    cout << "SizeOf DOUBLE       : " << sizeof(e) << endl;

    return 0;
}